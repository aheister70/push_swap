#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (!lst || !new)
		return ;
	new->next = *lst;
	new->prev = ft_lstlast(*lst);
	(*lst)->prev = new;
	*lst = new;
}
