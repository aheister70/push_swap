#include <time.h>
#include <stdlib.h>
#include <stdio.h>

int	check_earlier(int *nbrs, int num)
{
	int i;

	i = 0;
	while (nbrs[i] != '\0')
	{
		if (nbrs[i] == num)
			return(0);
		i++;
	}
	return(1);
}

int	*generate_numbers(int lower, int upper, int amount, int *nbrs)
{ 
    int i;
	int num;

	i = 0;
	srand(time(0));
	while (i < amount)
	{
        num = (rand() %(upper - lower + 1)) + lower;
		if (check_earlier(nbrs, num) == 1)
		{
			nbrs[i] = num;
			i++;
		}
	}
	printf("i = %d\n", i);
	return (nbrs);
}

int	main(void)
{
	int i;
	int *nbrs;

	i = 0;
	nbrs = malloc(500 * sizeof(int));
	generate_numbers(-2000, 5000, 500, nbrs);
	while (nbrs[i] != '\0')
	{
		printf("%d ", nbrs[i]);
		i++;
	}
	printf("\n");
	printf("i = %d\n", i);
	return (0);
}

